import { writeFile } from 'node:fs/promises';

const repository = 'disposable-email-domains/disposable-email-domains';
const fileName = 'disposable_email_blocklist.conf';
const commitResponse = await fetch(`https://api.github.com/repos/${repository}/commits/main`, {
	headers: { 'User-Agent': 'k1lla-mailplus-domain-updater' },
});

if (!commitResponse.ok) {
	throw new Error(`Unable to resolve the disposable-domain list revision: ${commitResponse.status}`);
}

const { sha } = await commitResponse.json();
const listResponse = await fetch(`https://raw.githubusercontent.com/${repository}/${sha}/${fileName}`);

if (!listResponse.ok) {
	throw new Error(`Unable to download the disposable-domain list: ${listResponse.status}`);
}

const domains = [...new Set((await listResponse.text())
	.split(/\r?\n/)
	.map((domain) => domain.trim().toLowerCase())
	.filter((domain) => domain && !domain.startsWith('#') && /^[a-z0-9.-]+$/.test(domain)))].sort();

if (domains.length === 0) {
	throw new Error('The disposable-domain list is empty.');
}

const content = `// Generated by scripts/update-disposable-domains.mjs. Do not edit manually.\n// Source: https://github.com/${repository} (${sha}), licensed CC0-1.0.\nexport const disposableDomainRevision = '${sha}';\nexport const disposableDomains = new Set(${JSON.stringify(domains)});\n`;
await writeFile(new URL('../src/const/disposable-domains.js', import.meta.url), content);
console.log(`Updated ${domains.length} disposable domains from ${sha}.`);
